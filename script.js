function logAge() {
    for (let age = 1; age <= 100; age++) {
        if (age <= 17) console.log(`${age} -ребенок`)
        if (age >= 18 && age <= 30) { console.log(`${age}-молод`) }
        if (age >= 31 && age <= 55) { console.log(`${age} -зрелый`) }
        if (age >= 56 && age <= 100) { console.log(`${age}-старый`) }
    }
}




