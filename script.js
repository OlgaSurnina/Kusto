function sayHallo(name, hour) {
    if (hour >= 6 && hour <= 10) { console.log(`Доброе утро, ${name}`) }
    else if (hour >= 11 && hour <= 16) { console.log(`Добрый день, ${name}`) }
    else if (hour >= 17 && hour <= 21) { console.log(`Добрый вечер, ${name}`) }
    else { console.log(`Доброй ночи, ${name}`) }
}

